package com.srdesai;
import java.util.Date;

public class Main {
    public static void main(String[] args) {
        int num1 = 10;
        double num2 = 3.14;
        boolean isTrue = true;
        char letter = 'A';
        String message = "Hello, World!";
        Date date = new Date();

        System.out.println("Integer variable: " + num1);
        System.out.println("Double variable: " + num2);
        System.out.println("Boolean variable: " + isTrue);
        System.out.println("Character variable: " + letter);
        System.out.println("String variable: " + message);
        System.out.println("Date variable: " + date);
    }
}
